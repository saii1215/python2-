# Create tuple
tup1 = (1, 2, 3)

# Access elements
print("First element:", tup1[0])

# Nested tuple
nested_tuple = (tup1, (4, 5, 6))
print("Nested tuple:", nested_tuple)

# Repetition
repeated = tup1 * 2
print("Repeated tuple:", repeated)

# Concatenation
tup2 = (7, 8)
concat = tup1 + tup2
print("Concatenated tuple:", concat)